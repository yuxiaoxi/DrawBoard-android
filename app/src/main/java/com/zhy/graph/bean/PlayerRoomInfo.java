public class PlayerRoomInfo {

    private String avater;
    private String guessWords;
    private String score;

    public String getAvater() {
        return avater;
    }

    public void setAvater(String avater) {
        this.avater = avater;
    }

    public String getGuessWords() {
        return guessWords;
    }

    public void setGuessWords(String guessWords) {
        this.guessWords = guessWords;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
